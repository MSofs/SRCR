resolve_aestrela(Nodo,Final, Caminho/Custo,TipoLixo) :-
	findall(Id,pontoRecolha(_,_,Id,_,Nodo,TipoLixo,_,_),R),
	aestrela([[(Nodo,R)]/0], Final,InvCaminho/Custo,TipoLixo),
	inverso(InvCaminho, Caminho).

aestrela(Caminhos,Final, Caminho,_) :-
	obtem_melhor(Caminhos, Caminho),
	Caminho = [(Nodo,_)|_]/_,Nodo==Final.

aestrela(Caminhos,Final, SolucaoCaminho,TipoLixo) :-
	obtem_melhor(Caminhos, MelhorCaminho),
	seleciona(MelhorCaminho, Caminhos, OutrosCaminhos),
	expande_aestrela(MelhorCaminho, ExpCaminhos,TipoLixo),
	append(OutrosCaminhos, ExpCaminhos, NovoCaminhos),
    aestrela(NovoCaminhos,Final, SolucaoCaminho,TipoLixo).

obtem_melhor([Caminho], Caminho) :- !.

obtem_melhor([Caminho1/Custo1,_/Custo2|Caminhos], MelhorCaminho) :-
	Custo1 =< Custo2, !,
	obtem_melhor([Caminho1/Custo1|Caminhos], MelhorCaminho).

obtem_melhor([_|Caminhos], MelhorCaminho) :-
	obtem_melhor(Caminhos, MelhorCaminho).

expande_aestrela(Caminho, ExpCaminhos,TipoLixo) :-
	findall(NovoCaminho, adjacente(Caminho,NovoCaminho,TipoLixo), ExpCaminhos).

adjacente([(Nodo,Id)|Caminho]/Custo, [(ProxNodo,R),(Nodo,Id)|Caminho]/NovoCusto,TipoLixo) :-
	ligacao(Nodo, ProxNodo, PassoCusto),\+ member(ProxNodo, Caminho),
	findall(ID,pontoRecolha(_,_,ID,_,ProxNodo,TipoLixo,_,_),R),
	NovoCusto is Custo + PassoCusto.

aTodasSolucoesL(Inicial,Final,T,L):- findall(C,(resolve_aestrela(Inicial,Final,C/Cu,T)),R),eliminaRepetidos(R,L).


resolveGulosa(Nodo, Final,Caminho/Custo, TipoLixo) :-
    findall(Id,pontoRecolha(_,_,Id,_,Nodo,TipoLixo,_,_),R),
    agulosa([[(Nodo,R)]/0],Final, CaminhoInverso/Custo,TipoLixo),
    inverso(CaminhoInverso, Caminho).

agulosa(Caminhos,Final, Caminho,_) :-
    obtem_melhor_g(Caminhos, Caminho),
    Caminho = [(Nodo,_)|_]/_,Nodo==Final.

agulosa(Caminhos,Final, SolucaoCaminho,TipoLixo) :-
    obtem_melhor_g(Caminhos, MelhorCaminho),
    seleciona(MelhorCaminho, Caminhos, OutrosCaminhos),
    expandeGulosa(MelhorCaminho, ExpCaminhos,TipoLixo),
    append(OutrosCaminhos, ExpCaminhos, NovoCaminhos),
    agulosa(NovoCaminhos,Final, SolucaoCaminho,TipoLixo).

obtem_melhor_g([Caminho], Caminho) :- !.

obtem_melhor_g([Caminho1/Custo1,_/_|Caminhos], MelhorCaminho) :-
	obtem_melhor_g([Caminho1/Custo1|Caminhos], MelhorCaminho).

obtem_melhor_g([_|Caminhos], MelhorCaminho) :- 
	obtem_melhor_g(Caminhos, MelhorCaminho).

expandeGulosa(Caminho, ExpCaminhos,TipoLixo) :-
	findall(NovoCaminho, adjacenteG(Caminho,NovoCaminho,TipoLixo), ExpCaminhos).

adjacenteG([(Nodo,Id)|Caminho]/Custo, [(ProxNodo,R),(Nodo,Id)|Caminho]/NovoCusto,TipoLixo) :-
	ligacao(Nodo, ProxNodo, PassoCusto),\+ member(ProxNodo, Caminho),
	findall(ID,pontoRecolha(_,_,ID,_,ProxNodo,TipoLixo,_,_),R),
	NovoCusto is Custo + PassoCusto.

gTodasSolucoesL(Inicial,Final,T,L):- findall(C,(resolveGulosa(Inicial,Final,C/Cu,T)),R),eliminaRepetidos(R,L).

g1TodasSolucoesL(Inicial,Final,T,L):- findall(C,(geraCircuito_tipoLixoG(Inicial,Final,C/Cu,T)),R),eliminaRepetidos(R,L).

