resolve_astar(PontoRecolha, Inicial, Final, CaminhoFinal/Custo) :-
	astarSearch(PontoRecolha,[[(Inicial,'Inicio')]/0], Final, CaminhoReversed/Custo),
        inverteLista(CaminhoReversed, CaminhoFinal, []),!.

astarSearch(PontoRecolha, Caminhos, Final, Caminho) :-
	bestPathAsearch(Caminhos, Caminho),
	Caminho = [(Nodo,C)|_]/_, Nodo == Final.

astarSearch(PontoRecolha, Caminhos, Final, SolucaoCaminho) :-
	bestPathAsearch(Caminhos, MelhorCaminho),
	seleciona(MelhorCaminho, Caminhos, OutrosCaminhos),
	expand_astar(PontoRecolha, MelhorCaminho, Final, ExpCaminhos),
	append(OutrosCaminhos, ExpCaminhos, NovoCaminhos),
    astarSearch(PontoRecolha, NovoCaminhos, Final, SolucaoCaminho).

bestPathAsearch([Caminho], Caminho) :- !.

bestPathAsearch([Caminho1/Custo1,_/Custo2|Caminhos], MelhorCaminho) :-
	Custo1  =< Custo2 , !,
	bestPathAsearch([Caminho1/Custo1|Caminhos], MelhorCaminho).

bestPathAsearch([_|Caminhos], MelhorCaminho) :-
	bestPathAsearch(Caminhos, MelhorCaminho).

expand_astar(PontoRecolha, Caminho, Final, ExpCaminhos) :-
	findall(NovoCaminho, adjacenteAStar(PontoRecolha, Caminho, Final, NovoCaminho), ExpCaminhos).

adjacenteAStar(PontoRecolha, [(Nodo,C1)|Caminho]/Custo, Final, [(ProxNodo,C2),(Nodo,C1)|Caminho]/NovoCusto) :-
	        verificarLigacaoAStar(PontoRecolha, Nodo, (ProxNodo,C2)),
            \+ membro((ProxNodo,C2), Caminho),
	        ligacao( Nodo, ProxNodo, PassoCusto),
            NovoCusto is Custo + PassoCusto.

