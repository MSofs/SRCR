% Depth-first Search ------ Profundidade

resolve_depthFirst(PontoRecolha, Inicial, Final, [(Inicial,R)|Caminho]) :-
	 procuraId(Inicial,PontoRecolha,L),eliminaRepetidos(L,R),
	 depthFirstSearch(PontoRecolha, Inicial, Final, Caminho),!.

depthFirstSearch(_, Final, Final, []) :- !.

depthFirstSearch(PontoRecolha, Nodo, Final, [(ProxNodo,R)|Caminho]) :-
    procuraId(ProxNodo,PontoRecolha,L),eliminaRepetidos(L,R),
    adjacenteDF(PontoRecolha, Nodo, ProxNodo),
    depthFirstSearch(PontoRecolha, ProxNodo, Final, Caminho).

adjacenteDF(_, Nodo, ProxNodo) :-
    ligacao(Nodo,ProxNodo,_).
