resolve_greedy(PontoRecolha, Inicial, Final, Caminho/Custo):-
	pontoRecolha(_,_,_,_,Inicial,_,_,Estima),
        greedySearch(PontoRecolha, [[(Inicial, 'Start')]/0/Estima], Final, InvCaminho/Custo/_),
	inverteLista(InvCaminho, Caminho, []),!.

greedySearch(PontoRecolha, Caminhos, Final, Caminho):-
        bestPathGreedy(Caminhos, Caminho),
        Caminho = [(Nodo,C)|_]/_/_, Nodo == Final.

greedySearch(PontoRecolha, Caminhos, Final, SolucaoCaminho):-
        bestPathGreedy(Caminhos, MelhorCaminho),
	seleciona(MelhorCaminho, Caminhos, OutrosCaminhos),
	expand_greedy(PontoRecolha, MelhorCaminho, Final, ExpCaminhos),
	append(OutrosCaminhos, ExpCaminhos, NovoCaminhos),
        greedySearch(PontoRecolha, NovoCaminhos, Final, SolucaoCaminho).

bestPathGreedy([Caminho],Caminho):- !.

bestPathGreedy([Caminho1/Custo1/Est1,_/Custo2/Est2|Caminhos], MelhorCaminho):-
        Est1 =< Est2, !,
        bestPathGreedy([Caminho1/Custo1/Est1|Caminhos], MelhorCaminho).

bestPathGreedy([_|Caminhos],MelhorCaminho):-
        bestPathGreedy(Caminhos, MelhorCaminho).

